    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        // TODO add your handling code here:
        
        //Pega as informações
        String nome = nomeCompleto.getText();
        String cpf = cpfPessoa.getText();
        String dataNascimento = dataNascimentoDigitado.getText();
        String telefone = telefoneDigitado.getText();
        String email = emailDigitado.getText();
        String crm = crmMedico.getText();
        String uf = ufCRM.getText();
        String dataInscricao = this.dataInscricaoMedico.getText();
        int especialidade = listaEspecialidades.getSelectedIndex();
        String dataContratacao = this.dataContratacaoMedico.getText();
        
        ProfissionalSaude profissional = controleProfissional.buscaProfissional(cpf);
        
        System.out.println("Verificação CPF: " + profissional);
        
        if(profissional == null){
            profissional = new ProfissionalSaude(cpf, nome, telefone, email, dataNascimento, dataContratacao);
            profissional = controleProfissional.cadastraProfissional(profissional, adm.getId());
            System.out.println("profissional com id = " + profissional);
            
            if(profissional == null){
                JOptionPane.showMessageDialog(null, "Ops! Algo deu errado!");
            } else {
                Medico medico = new Medico(crm, uf, dataInscricao, especialidade,
                        profissional.getId(), profissional.getCpf(), profissional.getNome(),
                        profissional.getTelefone(), profissional.getEmail(),
                        profissional.getDataNascimento(), profissional.getDataContratacao());
                
                boolean confirmacao = controleMedico.cadastraMedico(medico);
                
                if(confirmacao){
                    JOptionPane.showMessageDialog(null, "Médico Cadastrado com Sucesso!");
                    new TelaInicialAdministrador(adm).setVisible(true);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Ops! Algo deu errado!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Profissional Localizado!\n"
                    + "Nome: " + profissional.getNome());
        }
        
    }//GEN-LAST:event_cadastrarActionPerformed

